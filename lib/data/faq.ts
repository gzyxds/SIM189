/* ========== 工具函数 ========== */

/** 热门问题列表（前 5 条） */
export const HOT_FAQS = FAQS.filter((f) => f.isHot).slice(0, 5);

/** 按分类筛选 FAQ 列表 */
export function getFaqsByCategory(category: string): FaqItem[] {
    if (category === "热门问题") return HOT_FAQS;
    if (category === "全部") return FAQS;
    return FAQS.filter((f) => f.category === category);
}

/** 按关键词搜索 FAQ */
export function searchFaqs(keyword: string): FaqItem[] {
    const kw = keyword.trim().toLowerCase();
    if (!kw) return FAQS;
    return FAQS.filter(
        (f) => f.q.toLowerCase().includes(kw) || f.a.toLowerCase().includes(kw)
    );
}
